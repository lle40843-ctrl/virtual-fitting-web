const FAL_MODEL = process.env.FAL_MODEL || "fal-ai/image-apps-v2/virtual-try-on";
const HF_SPACE = process.env.HF_SPACE || "yisol/IDM-VTON";
const CATEGORY_DESCRIPTIONS = {
  upper: "upper body clothing, top, coat, jacket, shirt",
  lower: "lower body clothing, pants, trousers, skirt",
  dress: "full body dress, one-piece dress, gown, full outfit",
  shoes: "shoes, footwear",
};

function fileToDataUri(file) {
  const mimeType = file.type || "image/jpeg";
  return `data:${mimeType};base64,${file.content.toString("base64")}`;
}

function findImageUrl(value) {
  if (!value) return "";

  if (typeof value === "string") {
    return /^https?:\/\//i.test(value) ? value : "";
  }

  if (Array.isArray(value)) {
    for (const item of value) {
      const result = findImageUrl(item);
      if (result) return result;
    }
    return "";
  }

  if (typeof value === "object") {
    for (const key of ["url", "image_url", "try_on_image_url", "path"]) {
      const result = findImageUrl(value[key]);
      if (result) return result;
    }

    for (const key of ["image", "images", "output", "result", "data"]) {
      const result = findImageUrl(value[key]);
      if (result) return result;
    }
  }

  return "";
}

function normalizeHuggingFaceUrl(url) {
  if (!url) return "";
  if (/^https?:\/\//i.test(url)) return url;
  if (url.startsWith("/")) {
    return `https://huggingface.co/spaces/${HF_SPACE}${url}`;
  }
  return url;
}

function shouldFallbackToFreeProvider(error) {
  return /balance|exhausted|locked|payment|quota|FAL_KEY/i.test(error.message || "");
}

function categoryDescription(garmentCategory) {
  return CATEGORY_DESCRIPTIONS[garmentCategory] || CATEGORY_DESCRIPTIONS.upper;
}

async function callFalVirtualTryOn({ personPhoto, clothingPhoto }) {
  if (!process.env.FAL_KEY) {
    throw new Error("FAL_KEY is not configured");
  }

  const response = await fetch(`https://fal.run/${FAL_MODEL}`, {
    method: "POST",
    headers: {
      Authorization: `Key ${process.env.FAL_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      person_image_url: fileToDataUri(personPhoto),
      clothing_image_url: fileToDataUri(clothingPhoto),
      preserve_pose: true,
      aspect_ratio: "3:4",
    }),
  });

  const text = await response.text();
  let payload;
  try {
    payload = text ? JSON.parse(text) : {};
  } catch {
    payload = { rawText: text };
  }

  if (!response.ok) {
    const detail = payload.detail || payload.error || payload.message || response.statusText;
    throw new Error(`fal.ai generation failed: ${detail}`);
  }

  const tryOnImageUrl = findImageUrl(payload);
  if (!tryOnImageUrl) {
    throw new Error("fal.ai returned a result but no generated image URL was found");
  }

  return {
    mode: "fal",
    tryOnImageUrl,
    note: "fal.ai generated the try-on image",
    raw: payload,
  };
}

async function callHuggingFaceTryOn({ personPhoto, clothingPhoto, garmentCategory }) {
  const { Client, handle_file } = await import("@gradio/client");
  const options = process.env.HUGGINGFACE_TOKEN
    ? { token: process.env.HUGGINGFACE_TOKEN }
    : undefined;
  const app = await Client.connect(HF_SPACE, options);

  const personBlob = new Blob([personPhoto.content], {
    type: personPhoto.type || "image/jpeg",
  });
  const clothingBlob = new Blob([clothingPhoto.content], {
    type: clothingPhoto.type || "image/jpeg",
  });

  const result = await app.predict("/tryon", {
    dict: {
      background: handle_file(personBlob),
      layers: [],
      composite: null,
    },
    garm_img: handle_file(clothingBlob),
    garment_des: categoryDescription(garmentCategory),
    is_checked: true,
    is_checked_crop: false,
    denoise_steps: 30,
    seed: 42,
  });

  const tryOnImageUrl = normalizeHuggingFaceUrl(findImageUrl(result));
  if (!tryOnImageUrl) {
    throw new Error("Hugging Face returned no generated image URL");
  }

  return {
    mode: "huggingface",
    tryOnImageUrl,
    note: "Generated by free Hugging Face Space. It may be slow or unstable.",
    raw: result,
  };
}

async function callVirtualTryOn({ personPhoto, clothingPhoto, garmentCategory }) {
  try {
    return await callFalVirtualTryOn({ personPhoto, clothingPhoto });
  } catch (error) {
    if (!shouldFallbackToFreeProvider(error)) {
      throw error;
    }

    return callHuggingFaceTryOn({ personPhoto, clothingPhoto, garmentCategory });
  }
}

module.exports = {
  callFalVirtualTryOn,
  callHuggingFaceTryOn,
  callVirtualTryOn,
};
